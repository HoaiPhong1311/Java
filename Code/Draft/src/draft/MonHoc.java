
public class MonHoc {
    private String mon;
    private int tinChi;
    private double diem;

    public MonHoc(String mon, int tinChi, double diem) {
        this.mon = mon;
        this.tinChi = tinChi;
        this.diem = diem;
    }

    public String getMon() {
        return mon;
    }

    public void setMon(String mon) {
        this.mon = mon;
    }

    public int getTinChi() {
        return tinChi;
    }

    public void setTinChi(int tinChi) {
        this.tinChi = tinChi;
    }

    public double getDiem() {
        return diem;
    }

    public void setDiem(double diem) {
        this.diem = diem;
    }
    
}
